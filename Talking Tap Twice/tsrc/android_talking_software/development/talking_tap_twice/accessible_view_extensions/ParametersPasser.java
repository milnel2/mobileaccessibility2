package android_talking_software.development.talking_tap_twice.accessible_view_extensions;

/**
 * <h1>Class Overview</h1>
 * <p></br></br>This interface defines methods for retrieving parameters that are passed to the TTT. The idea is that an extension of a Layout, such as LinearLayout, will implement this interface and be used to pass parameters to the TTT.</br></br></p>
 * 
 * @author Nicole
 *
 */
public interface ParametersPasser 
{
/**
 * Returns the name of the method that AccessibleViewExtensions will invoke if their method field is set to default.
 * 
 * @return 
 */
public String getDefaultMethod();
/**
 * Returns the default phrase to be spoken when an AccessibleViewExtension is selected if its whenSelected field is set to default.
 * @return The default phrase to be spoken when an AccessibleViewExtension is selected if its whenSelected field is set to default.
 */
public String getDefaultSelected();
/**
 * Returns the String that is used to initialize the pronounciation dictionary of the TTT.
 * 
 * @return The String that is used to initialize the pronounciation dictionary of the TTT.
 */
public String getPronounciationDictionaryEntries();
}