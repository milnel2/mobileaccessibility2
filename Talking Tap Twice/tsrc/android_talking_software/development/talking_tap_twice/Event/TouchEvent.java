package android_talking_software.development.talking_tap_twice.Event;

/**
 * <h1>Class Overview</h1>
 *<p></br></br>An event which indicates that an AccessibleViewExtension has been touched. The event is passed to the TouchListener of the AccessibleViewExtension which registered to receive such events using the setListener method of the AccessibleViewExtension.</br></br></p> 
 * 
 * @author Nicole
 *
 */
public class TouchEvent 
{
	/**
	 * The name of the method for the TouchListener to invoke.
	 */
	public String method;
	/**
	 * The phrase for the TouchListener to speak.
	 */
	public String speak;
}